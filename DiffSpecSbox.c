void ANF(int truthtable[(1<<N)],int coeftable[(1<<N)]){
    // just NNF
    for (int i=0;i<(1<<N);i++)
        coeftable[i] = truthtable[i];
    for (int i=0;i<N;i++)
    {
        int gap = 1<<(N-i-1);
        for (int j=0;j<(2<<i);j=j+2)
        {
            for (int l=0;l<gap;l++)
            {
                coeftable[gap*j+l]     = coeftable[gap*j+l];
                coeftable[gap*(j+1)+l] = coeftable[gap*j+l] - coeftable[gap*(j+1)+l];
            }
        }
    }
    //ANF = NNF (mod 2)
    for (int i=0;i<(1<<N);i++)
        coeftable[i] &= 1;
}

void printPolynomial(int coeftable[(1<<N)]){
    int index_monomial[1<<N];
    int counter = 0;
    for (int i=0;i<(1<<N);i++)
    {
        if (coeftable[i])
        {
            index_monomial[counter] = i;
            counter++;
        }
    }
    for (int i=0;i<counter;i++)
    {
        int binaryarray[N];
        decimalTobinary(index_monomial[i],binaryarray,N);
        for (int j=0;j<N;j++)
            if (binaryarray[j])
                printf("x_%d",j);
        if (i != counter - 1)
            printf("+");
        else
            printf("\n");
    }
}


