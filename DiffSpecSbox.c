// it return the ortho_derivative of the sbox, note that the sbox must be APN!
void OrthoDerivative(int sbox[1<<N],int od[1<<N]){
    od[0] = 0;
    for (int a=1;a<(1<<N);a++)
    {
        int list[1<<N];
        for (int x=0;x<1<<N;x++)
            list[x] = sbox[x]^sbox[x^a]^sbox[a]^sbox[0];
        for (int x=1;x<1<<N;x++)
        {
            int count = 0;
            for (int i=0;i<1<<N;i++)
            {
                if ((InnerProduct(x,list[i])&1) != 0)
                    break;
                else
                    count++;
            }
            if (count == (1<<N))
            {
                od[a] = x;
                break;
            }
        }
    }
}

